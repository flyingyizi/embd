func (hd *LCD) changeGRB(color uint16) (p uint16) {

	red := (color & 0x1F)        //5 bits
	green := (color >> 5) & 0x3F //6 bits
	blue := (color >> 11) & 0x1F //5 bits

	p = ((red << 11) | (green << 6) | blue)
	return
}

func (hd *LCD) WriteAscii16x24Str(x, y uint16, str string, wordColor, backColor uint16) {

	for _, by := range str {
		wordNum := (by - 32)
		val := ASCII16x24[wordNum]

		hd.SetCursor(x, y, x+15, y+23)
		for _, color := range val {
			for j := 0; j < 8; j++ {
				if (color & 0x80) == 0x80 {
					hd.WriteData16(wordColor)
				} else {
					hd.WriteData16(backColor)
				}
				color <<= 1
			}
		}
		x += 16
	}
}

//DrawDot    draw a dot with color
func (hd *LCD) DrawDot(x, y uint16, color uint16) {
	if x <= 0 {
		x = 1
	}
	hd.SetCursor(x-1, y, x+2, y+2) //单个像素

	for i := 0; i < 16; i++ {
		hd.WriteData16(color)
	}
}

func (hd *LCD) DrawLine(xStart, yStart, xEnd, yEnd, color uint16) {
	//draw special colum or row
	if xStart == xEnd {
		if yEnd < yStart {
			yEnd, yStart = yStart, yEnd
		}
		for t := yStart; t <= yEnd+1; t++ {
			hd.DrawDot(xStart, t, color)
		}
		return
	} else if yStart == yEnd {
		if xEnd < xStart {
			xEnd, xStart = xStart, xEnd
		}
		for t := xStart; t <= xEnd+1; t++ {
			hd.DrawDot(t, yStart, color)
		}
		return
	}

	//draw normal line
	var yOrien bool
	deltaX := math.Abs(float64(xEnd) - float64(xStart))
	deltaY := math.Abs(float64(yEnd) - float64(yStart))
	if deltaX < deltaY {
		yOrien = true
		//base on y orien, adjustment dot mark
		if yEnd < yStart {
			xEnd, xStart = xStart, xEnd
			yEnd, yStart = yStart, yEnd
		}
	} else {
		yOrien = false
		if xEnd < xStart {
			xEnd, xStart = xStart, xEnd
			yEnd, yStart = yStart, yEnd
		}
	}

	//draw line, the line will be : y=ax+b
	//a=(y1-y0)/(x1-x0),   b= y1- a*x1
	var a, b float64

	a = (float64(yEnd) - float64(yStart)) / (float64(xEnd) - float64(xStart))
	b = float64(yEnd) - a*float64(xEnd)

	if yOrien == true {
		for t := uint16(0); t <= uint16(deltaY)+1; t++ {
			y := yStart + t
			x := (float64(y) - b) / a
			hd.DrawDot(uint16(x), uint16(y), color)
		}
	} else {
		for t := uint16(0); t <= uint16(deltaX)+1; t++ {
			x := xStart + t
			y := a*float64(x) + b
			hd.DrawDot(uint16(x), uint16(y), color)
		}

	}

}

// Clear clears the display and mode settings sets the cursor to the home position.
func (hd *LCD) Clear(color uint16) error {
	grb := hd.changeGRB(color)

	if err := hd.SetCursor(0, 0, LcdWidth, LcdHeight); err != nil {
		return err
	}

	var i uint16
	for i = 0; i < LcdHeight; i++ {
		for j := 0; j < LcdWidth; j++ {
			hd.R61526.WriteData16(grb)

		}
	}

	return nil
}

func (hd *LCD) ExecUserCmd(cmd byte, paramenters ...byte) error {
	if err := hd.R61526.WriteCmd(cmd); err != nil {
		return err
	}

	for _, param := range paramenters {
		if err := hd.R61526.WriteData(param); err != nil {
			return err
		}
	}
	return nil
}

