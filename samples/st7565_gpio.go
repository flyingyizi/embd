func modify(hd *st7565p.LCD) error {
	running := true
	reader := bufio.NewReader(os.Stdin)
	for running {
		data, _, _ := reader.ReadLine()
		command := string(data)
		if command == "stop" {
			running = false
		}
		abb, _ := strconv.Atoi(command)
		fmt.Printf("received value is %v\n", abb)

		//if err := hd.SetBoosterRatioMode(byte(abb)); err != nil {
		//	return err
		//}
	}
	return nil
}

